// Addition, subtraction, multiplication, division, incrementing
var sum = 10 + 10;
console.log(sum);

var product = 8 * 5;
console.log(product);

var quotient = 66 / 33;
console.log(quotient)

var myVar = 0;
//myVar = myVar + 1;
myVar++;
console.log(myVar);

myVar--;
console.log(myVar);

var ourDecimal = 5.7;
// "+=" works in javascript!
ourDecimal += .5;
console.log(ourDecimal);
ourDecimal *= 2; //can also use "/="
console.log(ourDecimal);

// '%' instead of '/' for remainder
var remainder;
remainder = 11 % 3;
console.log(remainder);